import { ReactNode } from "react";
import { cn } from "../../../lib/utils";

interface ButtonProps {
  children: ReactNode; // Button text or content
  size?: "sm" | "md" | "icon"; // Added icon size
  variant?: "primary" | "outline" | "olive"; // Button variant
  startIcon?: ReactNode; // Icon before the text
  endIcon?: ReactNode; // Icon after the text
  onClick?: (e?: React.MouseEvent) => void; // Click handler
  disabled?: boolean; // Disabled state
  className?: string; // Additional classes
  type?: "button" | "submit" | "reset";
  isLoading?: boolean;
}

const Button: React.FC<ButtonProps> = ({
  children,
  size = "md",
  variant = "olive",
  startIcon,
  endIcon,
  onClick,
  className = "",
  disabled = false,
  type = "button",
  isLoading = false,
}) => {
  // Size Classes
  const sizeClasses = {
    sm: "px-4 py-2.5 text-xs",
    md: "px-5 py-3 text-sm",
    icon: "p-0",
  };

  // Variant Classes
  const variantClasses = {
    primary:
      "bg-brand-500 text-white shadow-theme-xs hover:bg-brand-600 disabled:bg-brand-300",
    outline:
      "bg-white text-gray-700 ring-1 ring-inset ring-gray-300 hover:bg-gray-50 dark:bg-gray-800 dark:text-gray-400 dark:ring-gray-700 dark:hover:bg-white/[0.03] dark:hover:text-gray-300",
    olive:
      "text-white shadow-theme-xs disabled:opacity-50",
  };

  return (
    <button
      className={cn(
        "inline-flex items-center justify-center gap-2 rounded-lg transition font-medium",
        sizeClasses[size],
        variantClasses[variant],
        (disabled || isLoading) && "cursor-not-allowed opacity-50",
        className
      )}
      style={
        variant === "olive"
          ? {
            backgroundColor: "var(--color-button-primary)",
            ...(disabled || isLoading
              ? {}
              : {
                transition: "background-color 0.2s",
              }),
          }
          : undefined
      }
      onMouseEnter={(e) => {
        if (variant === "olive" && !disabled && !isLoading) {
          (e.currentTarget as HTMLButtonElement).style.backgroundColor =
            "var(--color-button-primary-hover)";
        }
      }}
      onMouseLeave={(e) => {
        if (variant === "olive" && !disabled && !isLoading) {
          (e.currentTarget as HTMLButtonElement).style.backgroundColor =
            "var(--color-button-primary)";
        }
      }}
      onClick={isLoading ? undefined : onClick}
      disabled={disabled || isLoading}
      type={type}
    >
      {isLoading && (
        <svg className="animate-spin -ml-1 mr-2 h-4 w-4 text-current" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
          <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
          <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
        </svg>
      )}
      {!isLoading && startIcon && <span className="flex items-center shrink-0">{startIcon}</span>}
      {children}
      {!isLoading && endIcon && <span className="flex items-center shrink-0">{endIcon}</span>}
    </button>
  );
};

export default Button;
